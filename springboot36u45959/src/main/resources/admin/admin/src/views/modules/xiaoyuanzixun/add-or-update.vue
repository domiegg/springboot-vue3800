import { 
	isIntNumer,
} from "@/utils/validate";
export default {
	data() {
		var validateIntNumber = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isIntNumer(value)) {
				callback(new Error("请输入整数"));
			} else {
				callback();
			}
		};
		return {
			id: '',
			type: '',
			
			
			ro:{
				biaoti : false,
				fengmian : false,
				neirong : false,
				xiangqing : false,
				fabushijian : false,
				discussnum : false,
			},
			
			
			ruleForm: {
				biaoti: '',
				fengmian: '',
				neirong: '',
				xiangqing: '',
				fabushijian: '',
			},
		

			
			rules: {
				biaoti: [
				],
				fengmian: [
				],
				neirong: [
				],
				xiangqing: [
				],
				fabushijian: [
				],
				discussnum: [
					{ validator: validateIntNumber, trigger: 'blur' },
