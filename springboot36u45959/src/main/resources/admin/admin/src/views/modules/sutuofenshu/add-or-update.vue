				if (data && data.code === 0) {
					var json = data.data;
					if(((json.jiaoshigonghao!=''&&json.jiaoshigonghao) || json.jiaoshigonghao==0) && this.$storage.get("role")!="管理员"){
						this.ruleForm.jiaoshigonghao = json.jiaoshigonghao
						this.ro.jiaoshigonghao = true;
